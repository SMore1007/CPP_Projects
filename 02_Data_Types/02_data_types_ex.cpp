#include <iostream>
using namespace std;

int main() {
    // Integer data type
    int age = 25;
    cout << "Age (int): " << age << endl;

    // Floating point data type
    float height = 5.9;
    cout << "Height (float): " << height << endl;

    // Double data type (more precision than float)
    double pi = 3.1415926535;
    cout << "Value of Pi (double): " << pi << endl;

    // Character data type
    char grade = 'A';
    cout << "Grade (char): " << grade << endl;

    // Boolean data type
    bool isPassed = true;
    cout << "Is Passed (bool): " << isPassed << endl;  // Output: 1 (true)

    // String using C++ string library
    string name = "Sagar";
    cout << "Name (string): " << name << endl;

    return 0;
}
