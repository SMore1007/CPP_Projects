#include<iostream>
using namespace std;

int main()
{
    int a = 7;
    int b = 2;

    cout <<"Addtion Operator a+b:" << a + b << endl;
    cout <<"Subtraction Operator a-b:" << a - b << endl;
    cout <<"Multiplication Operator a*b:" << a * b << endl;
    cout <<"Division Operator a/b:" << a / b << endl;
    cout <<"Modulus Operator (To get the remainder) a%b:" << a % b << endl;

    return 0;
}